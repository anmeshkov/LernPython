# Функция вывода журнала ставок в консоль
def print_game_journal(journal):
    print('------------------| Журнал ставок |------------------')
    for item in journal:
        print(f'Игра загадала: {item[0]}, моя попытка: {item[1]}, на счету: {item[2]} ')


# Настройки игры
game_settings = {
    'deposit': 10000,   # начальный депозит
    'win_money': 1000,  # количество единиц, начисляемых в случае победы
    'number_1': 2,      # первое число в промежутке
    'number_2': 12      # второе число в промежутке
}

# Список содержащий журнал ставок
game_journal = []

# Вызываем функцию start_game с заданными параметрами
start_game(game_settings['deposit'], game_settings['win_money'], game_settings['number_1'], game_settings['number_2'])
